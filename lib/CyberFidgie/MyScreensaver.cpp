const int myScreensaverApp_frameCount = 16;

const int myScreensaverApp_frameSpeed = 200;


MyScreensaverApp::MyScreensaverApp(ButtonManager& btnMgr) : 
    buttonManager(btnMgr), 
    display(HAL::displayProxy()), 
    lastFrameTime(0), 
    currentFrame(0) {
    instance = this;
}

void MyScreensaverApp::begin() {
    buttonManager.registerCallback(button_BottomLeftIndex, onButtonBackPressed);
    lastFrameTime = 0;
    currentFrame = 0;
}

void MyScreensaverApp::end() {
    buttonManager.unregisterCallback(button_BottomLeftIndex);
}

void MyScreensaverApp::update() {
    unsigned long now = millis();
    if (myScreensaverApp_frameCount <= 0) return; // No frames

    // Handle static image (1 frame)
    if (myScreensaverApp_frameCount == 1) {
        if (lastFrameTime == 0) { // Draw only once
            display.clear();
            if (myScreensaverApp_allFrames[0] != NULL) {
                display.drawXbm(0, 0, 128, 64, myScreensaverApp_allFrames[0]);
            } else {
                display.drawString(0, 0, "Bitmap Err!");
            }
            display.display();
            lastFrameTime = 1; // Set flag to indicate "drawn"
        }
        return;
    }

    // Handle animation
    if (lastFrameTime != 0 && (now - lastFrameTime < myScreensaverApp_frameSpeed)) {
        return; // Not time for next frame
    }
    
    lastFrameTime = now;
    display.clear();

    if (currentFrame < 0 || currentFrame >= myScreensaverApp_frameCount) {
        currentFrame = 0;
    }

    if (myScreensaverApp_allFrames[currentFrame] != NULL) {
        display.drawXbm(0, 0, 128, 64, myScreensaverApp_allFrames[currentFrame]);
    } else {
        display.drawString(0, 0, "Frame Err!");
        // ESP_LOGE(TAG_MAIN, "[MyScreensaverApp] Bitmap error at frame %d", currentFrame);
    }
    
    display.display();
    currentFrame = (currentFrame + 1) % myScreensaverApp_frameCount;
}

void MyScreensaverApp::onButtonBackPressed(const ButtonEvent& e) {
    if (e.eventType == ButtonEvent_Released) {
        if (instance) {
            // ESP_LOGI(TAG_MAIN, "[MyScreensaverApp] Back button pressed");
            instance->end(); // Unregister button
            MenuManager::instance().returnToMenu();
        }
        /* else {
            ESP_LOGE(TAG_MAIN, "[MyScreensaverApp] Back button pressed, but instance is null!");
        } */
    }
}