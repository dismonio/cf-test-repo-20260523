EyeApp::EyeApp(ButtonManager& btnMgr) : 
    buttonManager(btnMgr), 
    display(HAL::displayProxy()), 
    lastFrameTime(0), 
    currentFrame(0) {
    instance = this;
}

void EyeApp::begin() {
    buttonManager.registerCallback(button_BottomLeftIndex, onButtonBackPressed);
    lastFrameTime = 0;
    currentFrame = 0;
}

void EyeApp::end() {
    buttonManager.unregisterCallback(button_BottomLeftIndex);
}

void EyeApp::update() {
    unsigned long now = millis();
    if (eyeApp_frameCount <= 0) return; // No frames

    // Handle static image (1 frame)
    if (eyeApp_frameCount == 1) {
        if (lastFrameTime == 0) { // Draw only once
            display.clear();
            if (eyeApp_allFrames[0] != NULL) {
                display.drawXbm(0, 0, 128, 64, eyeApp_allFrames[0]);
            } else {
                display.drawString(0, 0, "Bitmap Err!");
            }
            display.display();
            lastFrameTime = 1; // Set flag to indicate "drawn"
        }
        return;
    }

    // Handle animation
    if (lastFrameTime != 0 && (now - lastFrameTime < eyeApp_frameSpeed)) {
        return; // Not time for next frame
    }
    
    lastFrameTime = now;
    display.clear();

    if (currentFrame < 0 || currentFrame >= eyeApp_frameCount) {
        currentFrame = 0;
    }

    if (eyeApp_allFrames[currentFrame] != NULL) {
        display.drawXbm(0, 0, 128, 64, eyeApp_allFrames[currentFrame]);
    } else {
        display.drawString(0, 0, "Frame Err!");
        // ESP_LOGE(TAG_MAIN, "[EyeApp] Bitmap error at frame %d", currentFrame);
    }
    
    display.display();
    currentFrame = (currentFrame + 1) % eyeApp_frameCount;
}

void EyeApp::onButtonBackPressed(const ButtonEvent& e) {
    if (e.eventType == ButtonEvent_Released) {
        if (instance) {
            // ESP_LOGI(TAG_MAIN, "[EyeApp] Back button pressed");
            instance->end(); // Unregister button
            MenuManager::instance().returnToMenu();
        }
        /* else {
            ESP_LOGE(TAG_MAIN, "[EyeApp] Back button pressed, but instance is null!");
        } */
    }
}